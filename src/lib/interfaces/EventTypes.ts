/**
 * Event type for TerraDraw control
 *
 * mode-changed: an event dispatched when TerraDraw mode is changed
 * feature-deleted: an event dispatched when features are deleted by the control
 * expanded: an event dispatched when the tool is expanded
 * collapsed: an event dispatched when the tool is collapsed
 */
export type EventType = 'mode-changed' | 'feature-deleted' | 'expanded' | 'collapsed';
